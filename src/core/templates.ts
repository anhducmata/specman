export function detectedStack(scan: ScanResult): string {
  const stackLines = scan.detectedStack.length > 0
    ? scan.detectedStack.map(t => `- **${t.name}** (detected from \`${t.source}\`, confidence: ${t.confidence})`).join('\n')
    : '- No technologies detected. Please add manually.';

  const fileLines = scan.detectedFiles.length > 0
    ? scan.detectedFiles.map(f => `- \`${f}\``).join('\n')
    : '- No known files detected.';

  return `# Detected Stack

> ⚠️ DRAFT — Auto-detected by \`specman init\`. Verify and correct as needed.

## Technologies
${stackLines}

## Detected Files
${fileLines}

## Project Structure
- Has source code: ${scan.hasSrc ? '✅' : '❌'}
- Has tests: ${scan.hasTests ? '✅' : '❌'}
- Has Docker: ${scan.hasDocker ? '✅' : '❌'}
- Has CI/CD: ${scan.hasCi ? '✅' : '❌'}
- Has API spec: ${scan.hasApi ? '✅' : '❌'}
- Has migrations: ${scan.hasMigrations ? '✅' : '❌'}
`;
}

export function assumptions(scan: ScanResult): string {
  const stackAssumptions = scan.detectedStack
    .map(t => `- [ ] This project uses **${t.name}** (${t.confidence} confidence, source: \`${t.source}\`)`)
    .join('\n');

  return `# Assumptions

> ⚠️ DRAFT — These are auto-generated assumptions. Review each item and mark as confirmed or incorrect.

## Stack Assumptions
${stackAssumptions || '- No stack assumptions generated.'}

## Architecture Assumptions
- [ ] The project follows a standard structure for its stack
- [ ] There are no unusual build or deployment requirements
${scan.hasDocker ? '- [ ] Docker is used for containerized deployment' : ''}
${scan.hasCi ? '- [ ] CI/CD is configured via GitHub Actions' : ''}
${scan.hasMigrations ? '- [ ] Database migrations are managed in the migrations/ directory' : ''}

## Domain Assumptions
- [ ] Domain rules have not been formally documented yet
- [ ] Business logic is embedded in application code

## Risks
- [ ] Auto-detected assumptions may be wrong — verify before approving
- [ ] Missing context about deployment, scaling, and security requirements
`;
}

