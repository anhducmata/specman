export function caseFromData(data: CaseData): string {
  const relatedSpecs = data.relatedSpecs.length > 0
    ? data.relatedSpecs.map(s => `- ${s}`).join('\n')
    : '<!-- No related specs -->';

  return `# Case: ${data.title}

## Status
${data.status}

## Problem
${data.problem}

## Context
${data.context}

## Root Cause
${data.rootCause}

## Solution
${data.solution}

## Before
${data.before || '<!-- Not provided -->'}

## After
${data.after || '<!-- Not provided -->'}

## Result
${data.result}

## When To Reuse
${data.whenToReuse}

## When NOT To Use
${data.whenNotToUse}

## Related Specs
${relatedSpecs}

## Tags
${data.tags.join(', ')}
`;
}

